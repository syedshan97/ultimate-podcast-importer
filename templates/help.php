<h3>Ultimate Podcast Importer Help & Documentation</h3>
<p>Welcome to the Ultimate Podcast Importer help section. Here are some guidelines:</p>
<ul>
    <li><strong>Adding a Feed:</strong> Use the "Add Feed" tab to enter your RSS feed URL, select an author and default category from the dropdowns, and choose import settings.</li>
    <li><strong>Manage Feeds:</strong> The "Manage Feeds" tab lists your feeds along with statistics (manual import timestamp, auto-fetch timestamp, and import counts). Use Edit or Delete to modify or remove a feed.</li>
    <li><strong>Logs:</strong> The "Logs" tab displays the import process log. This helps diagnose issues or see progress when processing large feeds.</li>
    <li><strong>Auto-Import:</strong> When enabled, the plugin uses WordPress cron (scheduled hourly) to check and import new episodes. Auto-fetch interval is set per feed.</li>
    <li><strong>Uninstall:</strong> If you uninstall the plugin, all plugin data (feed settings and logs) will be deleted. Imported posts will remain.</li>
</ul>
<p>For further support, please refer to our documentation or contact our support team.</p>